# Escape  sequence
# BackSlashes (\) are used to be able to inform python
# that whatever python reads after the (\)

# Example that implements \ for Making quotes in
# a string
weather = "It\'s \"kind of sunny\" outside."

print(weather)

# Example using \n and \t to make a
# specifically formatted text appear
# for the user.
wish = ("\nPerfect for a day out in the beach:\n\t "
        "-maybe even play some volleyball.")

print(wish)