package CopyPracticeCode;
public class PrintF 
{
	/*
	 * In this program we will be practicing printF formating 
	 * a simple and more direct way to format information that is being 
	 * displayed to the user.
	 */
	public static void main (String []args)
	{
		
		//Storage inputs
		double num2 = 519.23;
		double num3 = 60.10;
		double num4 = 1212.12;
		
		//PrintF format that displays numbers in an organized fashion
		System.out.printf("%5d %5d %5d", num4, num2, num3);
	}
}
